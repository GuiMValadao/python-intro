# API- Interface de Programação de Aplicações, é um conjunto de regras e definições
# que permite que diferentes aplicações de software ou componentes se comuniquem entre
# si. Funciona como um intermediário, permitindo que pedidos sejam feitos e
# respostas sejam recebidas entre diferentes sistemas de software.
# Exemplo prático: APIs de pagamento: facilita transações de comércio eletrônico
# através de diferentes plataformas de pagamentos. Ex: <https://vindi.github.io/api-docs/dist/#/>.
# APIs RESTful refere-se a APIs que seguem os princípios do REST(Representational
# State Transfer). São baseadas em padrões HTTP e utilizadas para interações web.
# Características:
#   * Uso dos métodos HTTP(GET, POST, PUT, DELETE) para operações CRUD(Create, Read, Update e Delete).
#   * Curva de aprendizado menor.
#   * Fácil de implementar.

# API SOAP(Simple Object Acess Protocol) é um protocolo que define um
# padrão para a troca de mensagens baseadas em XML. <https://www.w3schools.com/xml/xml_soap.asp>
# Características:
#   * Protocolo baseado em XML para troca de informações
#   * Independente de linguagem e plataforma de transporte
#   * Suporte para operações complexas e segurança avançadas
# API GraphQL - uma linguagem de consulta para sua API, e um servidor capaz
# de executar essas consultas, retornando apenas os dados especificados.
# Características:
#   * Permite que os clientes especifiquem exatamente quais dados querem.
#   * Eficiente na redução de solicitações e no tamanho dos dados transferidos.
#   * Flexível e fortemente tipada, facilitando a evolução das APIs.
#
# O tipo certo de API depende da necessidade específica do projeto, dos recursos
# disponíveis e da expertise da equipe. RESTful é popular pela simplicidade,
# SOAP é preferido para segurança e transações complexas e GraphQL é ideal para
# aplicações que requerem dados dinâmicos e personalizados.

# Verbos HTTP
# Em APIs RESTful, os verbos HTTP tem papeis específicos que se alinham com as
# operações CRUD.
# Convenções RESTful: GET para leitura, POST para criação, PUT/PATCH para
# atualização e DELETE para remoção. Essas convenções são fundamentais
# para o design de uma API RESTful bem projetada.
