# FastAPI é um moderno framework web para construção de APIs com Python,
# baseado em padrões como OpenAPI e JSON Schema. Usa tipagem estática para validar
# dados e gerar documentação automática.
# Pontos positivos do FastAPI:
#   * Alto desempenho: comparável ao NodeJS e superior ao Flask
#   * Geração de documentação automática: Suporta Open API para documentação
#   * Validação de dados e serialização: Usa Pydantic para validação de dados e
#       serialização automática
#   * Facilidade de uso
#   * Suporte a async/await: suporte nativo para operações assíncronas.
# Pontos negativos do FastAPI:
#   * Comunidade menor: em crescimento, sua comunidade é inferior à de outros
#       menos estabelecidos como Django
#   * Maturidade: pode faltar alguma maturidade e menos plugins que outros frameworks mais antigos
#   * Complexidade em projetos grandes: a gestão pode se tornar complexa em projetos
#       maiores e complexos.
