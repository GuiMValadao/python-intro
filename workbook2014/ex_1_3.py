# -----------------
# Área de um quarto
# -----------------

# Lê os valores fornecidos pelo usuário.
largura = float(input('Digite a largura do quarto em metros: '))
altura = float(input('Digite a altura do quarto em metros: '))

# Calcula a área.
area  = largura * altura

# Exibe o resultado.
print('Área do quarto é ', area, 'metros quadrados.')